# Addition
print(2+1)
# output: 3

# Subtraction
print(2-1)
# output: 1

# Multiplication
print(2*2)
# output: 4

# Division
print(3/2)
# output: 1.5

# Floor Division
print(7//4)
# output: 1

# Modulo
print(7 % 4)
# output: 3

# Powers
print(2**3)
# output: 8

# Order of Operations
print(2 + 10 * 10 + 3)
# output: 105

# Parentheses to specify orders
print((2+10) * (10+3))
# output: 156
